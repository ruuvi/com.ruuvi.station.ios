    func testGeneratedStringAccessorsResolveDefaultAndExplicitLocales() {
        let fiLocale = Locale(identifier: "fi")
        var values: [String] = []
        values.reserveCapacity(1966)

        collectGeneratedLocalizationAccessors(into: &values, fiLocale: fiLocale)

        XCTAssertEqual(values.count, 1966)
        XCTAssertTrue(values.contains(RuuviLocalization.temperature))
        XCTAssertTrue(values.contains(RuuviLocalization.temperature(fiLocale)))
    }

    func testImageAndColorAssetsExposeUIKitAndSwiftUIWrappers() {
        let imageAsset = RuuviAsset.beaverAdvice
        XCTAssertEqual(imageAsset.name, "beaver-advice")
        XCTAssertNotNil(UIImage(asset: imageAsset))
        XCTAssertNotNil(imageAsset.image)
        XCTAssertNotNil(imageAsset.image(compatibleWith: UITraitCollection()))
        _ = imageAsset.swiftUIImage
        _ = SwiftUI.Image(asset: imageAsset)
        _ = SwiftUI.Image(asset: imageAsset, label: Text("asset"))
        _ = SwiftUI.Image(decorative: imageAsset)

        let colorAsset = RuuviColor.primary
        XCTAssertEqual(colorAsset.name, "Primary")
        XCTAssertNotNil(UIColor(asset: colorAsset))
        XCTAssertNotNil(colorAsset.color)
        XCTAssertNotNil(colorAsset.color(compatibleWith: UITraitCollection()))
        _ = colorAsset.swiftUIColor
        _ = SwiftUI.Color(asset: colorAsset)
    }

