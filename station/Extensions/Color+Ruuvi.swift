import UIKit

extension UIColor {
    static var normalButton: UIColor {
        return UIColor(red: 21.0 / 255,
                       green: 141.0 / 255,
                       blue: 165.0 / 255,
                       alpha: 1)
    }

    static var disableButton: UIColor {
        return UIColor.darkGray
    }
}
